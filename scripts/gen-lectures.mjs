function esc(s) {
  return s.replace(/\\/g, '\\\\').replace(/'/g, "\\'").replace(/`/g, '\\`');
}

function genEntry([id, title, body, takeaway, keyPoints]) {
  const kp = keyPoints.map((k) => `      '${esc(k)}',`).join('\n');
  const ex = [
    [`薛老师，「${title}」到底在讲什么？`, body.slice(0, 120) + (body.length > 120 ? '……' : '')],
    ['能举个生活中的例子吗？', takeaway],
    ['我该怎么记住这一点？', `记住这句：${takeaway}`],
  ];
  const dlg = ex
    .flatMap(([q, a]) => [
      `      student('${esc(q)}'),`,
      `      teacher('${esc(a)}'),`,
    ])
    .join('\n');
  return `  qa(
    '${id}',
    '${esc(title)}',
    \`${body.replace(/`/g, '\\`')}\`,
    '${esc(takeaway)}',
    [
${kp}
    ],
    [
      ['${esc(ex[0][0])}', '${esc(ex[0][1])}'],
      ['${esc(ex[1][0])}', '${esc(ex[1][1])}'],
      ['${esc(ex[2][0])}', '${esc(ex[2][1])}'],
    ],
  ),`;
}

